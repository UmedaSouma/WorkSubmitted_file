#include "block3D.h"
#include "manager.h"

//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CBlock3D::CBlock3D()
{
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CBlock3D::~CBlock3D()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CBlock3D::Init()
{
	

	CModel::Init();

	SetModelAddress("data\\model\\block_block001.x");		// アドレスを保存しておく
	CModeldata* pModeldata = CManager::GetModeldata();		// modeldata のポインタを持ってくる
	int nIdx = pModeldata->Regist(GetModelAddress());		// モデルデータの登録
	BindModel(pModeldata->GetAddress(nIdx));				// モデル情報をセットする

	SetSize({ MAX_SIZE+5.0f ,MAX_SIZE ,MAX_SIZE });

	SetType(TYPE::BLOCK);

	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CBlock3D::Uninit()
{
	CModel::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CBlock3D::Update()
{
	CModel::Update();
}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CBlock3D::Draw()
{
	

	

	CModel::Draw();

	
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CBlock3D* CBlock3D::Create(D3DXVECTOR3 pos)
{
	CBlock3D* pBlock3D = new CBlock3D;

	pBlock3D->SetPos(pos);
	pBlock3D->Init();

	return pBlock3D;
}
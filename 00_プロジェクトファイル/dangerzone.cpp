#include "dangerzone.h"
#include "texture.h"
#include "manager.h"

//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CDangerZone::CDangerZone()
{
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CDangerZone::~CDangerZone()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CDangerZone::Init()
{
	int nTexIdx = 0;

	CObject3D::Init();

	// テクスチャの設定
	CTexture* pTex = CManager::GetTexture();
	nTexIdx = pTex->Regist("data\\TEXTURE\\UI_dangerzone000.png");
	BindTexture(pTex->GetAddress(nTexIdx));

	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CDangerZone::Uninit()
{
	CObject3D::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CDangerZone::Update()
{
	CObject3D::Update();
}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CDangerZone::Draw()
{
	CObject3D::Draw();
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CDangerZone* CDangerZone::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size)
{
	CDangerZone* pDangerZone = new CDangerZone;

	pDangerZone->SetSize(size);	// サイズの設定
	pDangerZone->SetPos({ pos.x - size.x,pos.y,pos.z });	// 位置の設定

	// 初期化処理
	pDangerZone->Init();

	return pDangerZone;
}
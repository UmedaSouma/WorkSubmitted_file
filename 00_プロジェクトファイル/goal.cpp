#include "goal.h"
#include "manager.h"

//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CGoal::CGoal()
{
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CGoal::~CGoal()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CGoal::Init()
{
	// モデルデータをセット
	SetModelAddress("data\\model\\gimmick_goal.x");		// アドレスを保存しておく
	CModeldata* pModeldata = CManager::GetModeldata();
	int nIdx = pModeldata->Regist(GetModelAddress());
	BindModel(pModeldata->GetAddress(nIdx));

	CModel::Init();

	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CGoal::Uninit()
{
	CModel::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CGoal::Update()
{
	CModel::Update();
}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CGoal::Draw()
{
	CModel::Draw();
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CGoal* CGoal::Create(D3DXVECTOR3 pos)
{
	CGoal* pGoal = new CGoal;
	pGoal->SetPos(pos);

	pGoal->Init();

	return pGoal;
}

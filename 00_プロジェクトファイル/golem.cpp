#include "golem.h"
#include "manager.h"

//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CGolem::CGolem()
{
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CGolem::~CGolem()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CGolem::Init()
{
	SetTag(TAG_GOLEM);

	SetMaxLife(60);

	CEnemyland3D::Init();

	SetSize({ 20.0f,50.0f,20.0f });

	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CGolem::Uninit()
{
	CEnemyland3D::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CGolem::Update()
{
	int dire = GetDirection();	// 向きを取得する

	if (GetState() == STATE_MOVE)
	{
		SetMove({
		dire * 0.3f
		,GetMove().y
		,0.0f
			});
	}

	CEnemyland3D::Update();

}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CGolem::Draw()
{
	CEnemyland3D::Draw();
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CGolem* CGolem::Create(D3DXVECTOR3 pos)
{
	CGolem* pGolem = new CGolem;

	pGolem->SetPos(pos);

	pGolem->Init();

	return pGolem;
}
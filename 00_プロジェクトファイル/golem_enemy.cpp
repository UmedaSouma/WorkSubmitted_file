#include "golem_enemy.h"
#include "manager.h"

//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CGolemEnemy::CGolemEnemy()
{
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CGolemEnemy::~CGolemEnemy()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CGolemEnemy::Init()
{
	CGolem::Init();

	// モデルデータをセット
	SetModelAddress("data\\model\\enemy_golem000.x");	// アドレスを保存しておく
	CModeldata* pModeldata = CManager::GetModeldata();	// modeldata のポインタを持ってくる
	int nIdx = pModeldata->Regist(GetModelAddress());	// モデルデータの登録
	BindModel(pModeldata->GetAddress(nIdx));		 	// モデル情報をセットする

	SetTag(CEnemy3D::TAG_GOLEM);

	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CGolemEnemy::Uninit()
{
	CGolem::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CGolemEnemy::Update()
{
	CGolem::Update();
}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CGolemEnemy::Draw()
{
	CGolem::Draw();
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CGolemEnemy* CGolemEnemy::Create(D3DXVECTOR3 pos)
{
	CGolemEnemy* pGolemEnemy = new CGolemEnemy;

	pGolemEnemy->SetPos(pos);

	pGolemEnemy->Init();
	pGolemEnemy->SetType(ENEMY);


	return pGolemEnemy;
}
#include "start.h"
#include "manager.h"

//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CStart::CStart()
{
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CStart::~CStart()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CStart::Init()
{
	// モデルデータをセット
	SetModelAddress("data\\model\\gimmick_start.x");		// アドレスを保存しておく
	CModeldata* pModeldata = CManager::GetModeldata();
	int nIdx = pModeldata->Regist(GetModelAddress());
	BindModel(pModeldata->GetAddress(nIdx));

	CModel::Init();

	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CStart::Uninit()
{
	CModel::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CStart::Update()
{
	CModel::Update();
}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CStart::Draw()
{
	CModel::Draw();
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CStart* CStart::Create(D3DXVECTOR3 pos)
{
	CStart* pStart = new CStart;
	pStart->SetPos(pos);

	pStart->Init();

	return pStart;
}

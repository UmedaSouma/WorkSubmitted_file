#include "titleBG.h"
#include "texture.h"
#include "manager.h"

//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CTitleBG::CTitleBG(int nPriority) : CObject2D(nPriority)
{
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CTitleBG::~CTitleBG()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CTitleBG::Init()
{
	int nIdx = 0;

	CTexture* pTexData = CManager::GetTexture();
	nIdx = pTexData->Regist("data\\TEXTURE\\titleBG_000.png");
	BindTexture(pTexData->GetAddress(nIdx));

	SetSize({ SCREEN_WIDTH ,SCREEN_HEIGHT ,0.0f });
	SetPos({ SCREEN_WIDTH * 0.5f,SCREEN_HEIGHT * 0.5f,0.0f });

	CObject2D::Init();


	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CTitleBG::Uninit()
{

	CObject2D::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CTitleBG::Update()
{
	CObject2D::Update();
}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CTitleBG::Draw()
{
	CObject2D::Draw();
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CTitleBG* CTitleBG::Create()
{
	CTitleBG* pTitleBG = new CTitleBG;

	pTitleBG->Init();

	return pTitleBG;
}